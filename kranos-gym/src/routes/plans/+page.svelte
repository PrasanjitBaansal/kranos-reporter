<script>
	import { enhance } from '$app/forms';
	import { invalidateAll } from '$app/navigation';
	import { showSuccess, showError } from '$lib/stores/toast.js';
	import Modal from '$lib/components/Modal.svelte';
	
	export let data;
	export let form;
	
	let selectedPlan = null;
	let isEditing = false;
	let isLoading = false;
	let searchTerm = '';
	let formErrors = {};
	let showModal = false;
	
	// Form input tracking for real-time display name generation
	let currentName = '';
	let currentDuration = '';
	
	$: plans = data.groupPlans || [];
	$: filteredPlans = plans.filter(plan => 
		plan.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
		(plan.display_name && plan.display_name.toLowerCase().includes(searchTerm.toLowerCase()))
	);
	
	// Real-time display name generation
	$: autoGeneratedDisplayName = generateDisplayName(currentName, currentDuration);
	
	function editPlan(plan) {
		selectedPlan = plan;
		isEditing = true;
		formErrors = {};
		// Update current form values for display name generation
		currentName = plan.name || '';
		currentDuration = plan.duration_days || '';
		showModal = true;
	}
	
	function newPlan() {
		selectedPlan = null;
		isEditing = false;
		formErrors = {};
		// Reset current form values
		currentName = '';
		currentDuration = '';
		showModal = true;
	}

	function closeModal() {
		showModal = false;
		selectedPlan = null;
		isEditing = false;
		formErrors = {};
		currentName = '';
		currentDuration = '';
	}
	
	function validateForm(formData) {
		const errors = {};
		const name = formData.get('name');
		const duration_days = formData.get('duration_days');
		const default_amount = formData.get('default_amount');

		// Name validation
		if (!name || !name.trim()) {
			errors.name = 'Plan name is required';
		}

		// Duration validation
		if (!duration_days || duration_days <= 0 || !Number.isInteger(Number(duration_days))) {
			errors.duration_days = 'This field is required';
		}

		// Default amount validation (optional field)
		if (default_amount && default_amount.toString().trim() !== '') {
			if (default_amount < 0 || isNaN(Number(default_amount))) {
				errors.default_amount = 'Default amount must be a positive number';
			}
		}

		// Display name uniqueness validation
		if (name && duration_days) {
			const generatedDisplayName = generateDisplayName(name.trim(), duration_days);
			const existingPlan = plans.find(plan => 
				plan.display_name === generatedDisplayName && 
				(!isEditing || plan.id !== selectedPlan?.id)
			);
			if (existingPlan) {
				errors.name = 'A plan with this name and duration already exists';
			}
		}

		return errors;
	}

	const submitForm = () => {
		return async ({ formData, result }) => {
			// Client-side validation
			const errors = validateForm(formData);
			if (Object.keys(errors).length > 0) {
				formErrors = errors;
				return;
			}

			isLoading = true;
			formErrors = {};

			if (result.type === 'success') {
				showSuccess(isEditing ? 'Plan updated successfully!' : 'Plan created successfully!');
				closeModal();
				await invalidateAll();
			} else if (result.type === 'failure') {
				showError(form?.error || 'Failed to save plan. Please try again.');
			}
			isLoading = false;
		};
	};
	
	function generateDisplayName(name, duration) {
		return name && duration ? `${name} - ${duration} days` : '';
	}
</script>

<svelte:head>
	<title>Plans - Kranos Gym</title>
