<Modal bind:show={showModal} title={isEditing ? 'Edit Plan' : 'Add New Plan'} on:close={closeModal}>
	<form 
		method="post" 
		action={isEditing ? '?/update' : '?/create'}
		use:enhance={submitForm}
		class="plan-form-content"
		novalidate
	>
		{#if isEditing}
			<input type="hidden" name="id" value={selectedPlan?.id} />
		{/if}

		<div class="form-group">
			<label for="name" class="form-label">Plan Name *</label>
			<input
				type="text"
				id="name"
				name="name"
				bind:value={currentName}
				class="form-control"
				class:error={formErrors.name}
				placeholder="e.g., MMA Focus, Boxing Basics"
				on:input={() => {
					if (formErrors.name) {
						formErrors = { ...formErrors, name: '' };
					}
				}}
			/>
			{#if formErrors.name}
				<span class="error-message">{formErrors.name}</span>
			{/if}
		</div>

		<div class="form-group">
			<label for="duration_days" class="form-label">Duration (Days) *</label>
			<input
				type="number"
				id="duration_days"
				name="duration_days"
				bind:value={currentDuration}
				class="form-control"
				class:error={formErrors.duration_days}
				placeholder="e.g., 30, 90, 180"
				on:input={() => {
					if (formErrors.duration_days) {
						formErrors = { ...formErrors, duration_days: '' };
					}
				}}
			/>
			{#if formErrors.duration_days}
				<span class="error-message">{formErrors.duration_days}</span>
			{/if}
		</div>

		<div class="form-group">
			<label for="default_amount" class="form-label">Default Amount (‚Çπ)</label>
			<input
				type="number"
				id="default_amount"
				name="default_amount"
				value={selectedPlan?.default_amount || ''}
				class="form-control"
				class:error={formErrors.default_amount}
				placeholder="e.g., 149.99"
				on:input={() => {
					if (formErrors.default_amount) {
						formErrors = { ...formErrors, default_amount: '' };
					}
				}}
			/>
			{#if formErrors.default_amount}
				<span class="error-message">{formErrors.default_amount}</span>
			{/if}
		</div>

		<div class="form-group">
			<label for="status" class="form-label">Status</label>
			<select
				id="status"
				name="status"
				class="form-control"
				value={selectedPlan?.status || 'Active'}
			>
				<option value="Active">Active</option>
				<option value="Inactive">Inactive</option>
			</select>
		</div>

		<div class="form-group">
			<label for="display_name" class="form-label">Display Name</label>
			<input type="hidden" name="display_name" value={autoGeneratedDisplayName} />
			<div class="form-control disabled">
				{autoGeneratedDisplayName || 'Auto-generated from plan name and duration'}
			</div>
			<small class="form-hint">Auto-generated: "Plan Name - X days"</small>
		</div>


		<div class="form-actions">
			<button 
				type="button" 
				class="btn btn-secondary"
				on:click={closeModal}
				disabled={isLoading}
			>
				Cancel
			</button>
			<button 
				type="submit" 
				class="btn btn-primary"
				disabled={isLoading}
			>
				{#if isLoading}
					<span class="loading-spinner"></span>
				{/if}
				<span class="btn-icon">{isEditing ? 'üíæ' : '‚ûï'}</span>
				{isEditing ? 'Update Plan' : 'Create Plan'}
			</button>
		</div>
	</form>
</Modal>

